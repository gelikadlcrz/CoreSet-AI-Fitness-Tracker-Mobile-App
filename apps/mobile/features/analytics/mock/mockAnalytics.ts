import {
  calculateEstimated1RM,
  calculateTimeUnderTension,
  calculateTotalVolume,
  calculateVLwD,
} from '../calculations/calculateVLwD';
import { AnalyticsDataset, SetHistoryItem } from '../types/analytics.types';

function makeSet(
  id: string,
  exerciseName: string,
  performedAt: string,
  weightKg: number,
  reps: number,
  rpe: number,
  verificationStatus: 'ai_verified' | 'manual',
  averageDisplacementM: number,
  averageRepDurationSec: number,
): SetHistoryItem {
  return {
    id,
    exerciseName,
    performedAt,
    weightKg,
    reps,
    rpe,
    verificationStatus,
    averageDisplacementM,
    averageRepDurationSec,
    estimated1RM: calculateEstimated1RM(weightKg, reps),
    totalVolume: calculateTotalVolume(weightKg, reps),
    vlwd: calculateVLwD(weightKg, reps, averageDisplacementM),
    tut: calculateTimeUnderTension(reps, averageRepDurationSec),
  };
}

