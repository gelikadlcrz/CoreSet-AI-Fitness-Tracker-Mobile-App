function makeRepMetrics(
  setId: string,
  reps: number,
  baseDisplacementM: number,
  baseDurationSec: number,
) {
  return Array.from({ length: reps }, (_, index) => {
    const offset = index % 2 === 0 ? 0.01 : -0.01;
    const durationOffset = index % 3 === 0 ? 0.15 : -0.05;

    return {
      rep_metric_id: `${setId}_rep_${index + 1}`,
      set_id: setId,
      rep_index: index + 1,
      displacement_m: Number((baseDisplacementM + offset).toFixed(2)),
      duration_sec: Number((baseDurationSec + durationOffset).toFixed(2)),
      eccentric_sec: Number(((baseDurationSec + durationOffset) * 0.55).toFixed(2)),
      concentric_sec: Number(((baseDurationSec + durationOffset) * 0.45).toFixed(2)),
      peak_angular_velocity_deg_s: 180 + index * 4,
      created_at: '2026-10-24T18:20:00.000Z',
    };
  });
}

function makeBodyMetric(
  id: string,
  measuredAt: string,
  bodyWeightKg: number,
  bodyFatPercent: number,
) {
  return {
    body_metric_id: id,
    user_id: 'demo_user_001',
    measured_at: measuredAt,
    body_weight_kg: bodyWeightKg,
    body_fat_percent: bodyFatPercent,
    created_at: measuredAt,
    updated_at: measuredAt,
  };
}
